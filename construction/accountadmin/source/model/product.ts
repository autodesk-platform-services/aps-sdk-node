

/**
 * A list of ACC products the user is associated with.
 * @export
 * @interface Product
 */
export interface Product {
    /**
     * 
     * @type {string}
     * @memberof Product
     */
    'key'?: string;
    /**
     * 
     * @type {string}
     * @memberof Product
     */
    'icon'?: string;
    /**
     * 
     * @type {string}
     * @memberof Product
     */
    'name'?: string;
    /**
     * 
     * @type {Array<string>}
     * @memberof Product
     */
    'projectIds'?: Array<string>;
}

