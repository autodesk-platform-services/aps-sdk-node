/**
 * An object containing the ``id`` and ``type`` properties of a resource.
 * @export
 * @interface JsonApiTypeId
 */
export interface JsonApiTypeId {
    /**
     * The URN of the resource.
     * @type {string}
     * @memberof JsonApiTypeId
     */
    'id'?: string;
    /**
     * The type of the resource.
     * @type {string}
     * @memberof JsonApiTypeId
     */
    'type'?: string;
}

